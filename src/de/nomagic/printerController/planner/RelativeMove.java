package de.nomagic.printerController.planner;

/**
 * @author Lars P&ouml;tter
 * (<a href=mailto:Lars_Poetter@gmx.de>Lars_Poetter@gmx.de</a>)
 */
public class RelativeMove
{
    private final double x;
    private final double y;
    private final double z;
    private final double a;
    private final double b;
    private final double c;
    private final double e;

    public RelativeMove(final double x,
                        final double y,
                        final double z,
                        final double a,
                        final double b,
                        final double c,
                        final double e)
    {
        this.x = x;
        this.y = y;
        this.z = z;
        this.a = a;
        this.b = b;
        this.c = c;
        this.e = e;
    }

    public double getX()
    {
        return x;
    }

    public double getY()
    {
        return y;
    }

    public double getZ()
    {
        return z;
    }

    public double getA()
    {
        return a;
    }

    public double getB()
    {
        return b;
    }

    public double getC()
    {
        return c;
    }

    public double getE()
    {
        return e;
    }

}
